package variable;

public class Int {

	public static void main(String[] args) {
		int a; //interger : 숫자 데이터 형식, 변수 a를 선언
		a = 1;
		System.out.println(a+1); //2
		
		a = 2;
		System.out.println(a+1); //3
	}
}

